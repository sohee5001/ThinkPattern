package kr.co.thinkpattern.vo;

public class PatternVO {
	private int idx;
	private String p_name;
	private String p_contents;
	private String p_strong;
	private String p_weak;

	public int getIdx() {
		return idx;
	}

	public void setIdx(int idx) {
		this.idx = idx;
	}

	public String getP_name() {
		return p_name;
	}

	public void setP_name(String p_name) {
		this.p_name = p_name;
	}

	public String getP_contents() {
		return p_contents;
	}

	public void setP_contents(String p_contents) {
		this.p_contents = p_contents;
	}

	public String getP_strong() {
		return p_strong;
	}

	public void setP_strong(String p_strong) {
		this.p_strong = p_strong;
	}

	public String getP_weak() {
		return p_weak;
	}

	public void setP_weak(String p_weak) {
		this.p_weak = p_weak;
	}

}
